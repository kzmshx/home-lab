// Happy Mac / Sad Mac pixel art as SVG
// Based on the original 1984 Macintosh icons

const PIXEL = 2; // scale factor for each "pixel"

function createPixelArt(grid, color = '#33ff33') {
  const rows = grid.length;
  const cols = grid[0].length;
  const width = cols * PIXEL;
  const height = rows * PIXEL;

  let rects = '';
  for (let y = 0; y < rows; y++) {
    for (let x = 0; x < cols; x++) {
      if (grid[y][x]) {
        rects += `<rect x="${x * PIXEL}" y="${y * PIXEL}" width="${PIXEL}" height="${PIXEL}" fill="${color}"/>`;
      }
    }
  }

  return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${width} ${height}">${rects}</svg>`;
}

